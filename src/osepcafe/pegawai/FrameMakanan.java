    class Makanan extends FrameMakanan{
        int id_makanan, harga;
        String nama_makanan, status;
        
        public Makanan() {
            this.nama_makanan = txtNamaMakanan.getText();
            this.harga = Integer.parseInt(txtHargaMakanan.getText());
            this.status = comboMakanan.getSelectedItem().toString();
        }
    }
    
    public void refreshTable() {
        // Initialize the table model
        model = new DefaultTableModel();
        model.addColumn("ID Makanan");
        model.addColumn("Nama");
        model.addColumn("Harga");
        model.addColumn("Status");

        tblMakanan.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("SELECT * FROM makanan");
            this.rs = this.stat.executeQuery();

            while (rs.next()) {
                Object[] data = {
                    rs.getInt("id_makanan"),
                    rs.getString("nama_makanan"),
                    rs.getInt("harga"),
                    rs.getString("status")
                };
                model.addRow(data);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        txtIdMakanan.setText("");
        txtNamaMakanan.setText("");
        txtHargaMakanan.setText("");
        
    }
