    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            Makanan u = new Makanan();
            this.stat = k.getCon().prepareStatement("insert into makanan values(?,?,?,?)");
            stat.setInt(1, 0); 
            stat.setString(2, u.nama_makanan);
            stat.setInt(3, u.harga);
            stat.setString(4, u.status);
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
           JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            Makanan u = new Makanan();
        this.stat = k.getCon().prepareStatement("UPDATE makanan SET nama_makanan=?, harga=?, status=? WHERE id_makanan=?");
        stat.setString(1, u.nama_makanan);
        stat.setInt(2, u.harga);
        stat.setString(3, u.status);
        stat.setInt(4, Integer.parseInt(txtIdMakanan.getText()));
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
          JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

