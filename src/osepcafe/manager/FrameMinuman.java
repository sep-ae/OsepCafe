    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            Minuman u = new Minuman();
            this.stat = k.getCon().prepareStatement("insert into minuman values(?,?,?,?)");
            stat.setInt(1, 0); 
            stat.setString(2, u.nama_minuman);
            stat.setInt(3, u.harga);
            stat.setString(4, u.status);
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
           JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            Minuman u = new Minuman();
        this.stat = k.getCon().prepareStatement("UPDATE minuman SET nama_minuman=?, harga=?, status=? WHERE id_minuman=?");
        stat.setString(1, u.nama_minuman);
        stat.setInt(2, u.harga);
        stat.setString(3, u.status);
        stat.setInt(4, Integer.parseInt(txtIdMinuman.getText()));
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
          JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

