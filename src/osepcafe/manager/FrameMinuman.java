        class Minuman extends FrameMinuman{
        int id_minuman, harga;
        String nama_minuman, status;
        
        public Minuman() {
            this.nama_minuman = txtNamaMinuman.getText();
            this.harga = Integer.parseInt(txtHargaMinuman.getText());
            this.status = comboMinuman.getSelectedItem().toString();
        }
    }
        
    public void refreshTable() {
        // Initialize the table model
        model = new DefaultTableModel();
        model.addColumn("ID Minuman");
        model.addColumn("Nama");
        model.addColumn("Harga");
        model.addColumn("Status");

        tblMinuman.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("SELECT * FROM minuman");
            this.rs = this.stat.executeQuery();

            while (rs.next()) {
                Object[] data = {
                    rs.getInt("id_minuman"),
                    rs.getString("nama_minuman"),
                    rs.getInt("harga"),
                    rs.getString("status")
                };
                model.addRow(data);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        txtIdMinuman.setText("");
        txtNamaMinuman.setText("");
        txtHargaMinuman.setText("");
        
    }
